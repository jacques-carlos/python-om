# if / elif / else
# BLOCOS DE CÓDIGO
# if condição -> retorna um boll (True/False)

entrada = input('Você quer "entrar" ou "sair"? ')

if entrada == 'entrar':
    print('Você entrou no sistema.')
elif entrada == 'sair':
    print('Você saiu no sistema.')
else:
    print('Erro! Tente novamente.')

print('FORA DOS BLOCOS')
