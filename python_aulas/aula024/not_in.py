# Operadores in e not in
# Strings são iteráveis
#  0  1  2  3  4  5
#  O  t  á  v  i  o
# -6 -5 -4 -3 -2 -1

nome = 'Otávio'
print(nome[3])
print(nome[-3])

print('v' not in nome)  # retorna True ou False
print('x' not in nome)  # retorna True ou False
print('Ot' not in nome)  # retorna True ou False
print('oi' not in nome)  # retorna True ou False
